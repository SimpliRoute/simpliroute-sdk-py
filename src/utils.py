def sum_two(n1, n2):
    """
    :type n1: float or int
    :param n1: First number to sum.

    :type n2: float or int
    :param n2: Second number to sum.

    :returns: A number representing the sum of both parameters.
    """
    return n1 + n2


if __name__ == "__main__":
    print(sum_two(1, 9))
